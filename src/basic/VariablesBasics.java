package basic;

public class VariablesBasics {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        int y;
        y = 10;
        System.out.println(y);
        System.out.println(x*y);
        x = 7;
        System.out.println(x*y);
        y = 3;
        System.out.println(x*y);
        x = x*2;
        System.out.println(x);
        x = x+2;
        System.out.println(x);
        x = x-2;
        System.out.println(x);
        x = x/2;
        System.out.println(x);


    }

}
