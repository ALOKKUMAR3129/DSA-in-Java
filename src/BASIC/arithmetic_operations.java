package BASIC;

public class arithmetic_operations {
    static void main(String[] args) {
        int x = 5, y = 3;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);

        System.out.println("-----------");

        double a = 5, b = 3;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);


    }
}
